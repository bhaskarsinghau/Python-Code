# min(), max(), sum()

val = [17, 24, 15, 30]
print("The Minimum value of the  list:",val,":",min(val))
print("The Maximum Value of the list:",val,":",max(val))
print("The Sum of the list:",val,":",sum(val))

# Output: The Minimum value of the  list: [17, 24, 15, 30] : 15
# The Maximum Value of the list: [17, 24, 15, 30] : 30
# The Sum of the list: [17, 24, 15, 30] : 86