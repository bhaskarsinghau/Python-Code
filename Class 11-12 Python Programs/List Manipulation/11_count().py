
# This function returns the count of the item.
# if the given item is not in the list, it returns zero.

a = [13,14,12,18,20,10,18,23]
b = a.count(18)
print("18 comes",b,"times in the list.")

# Output: 18 comes 2 times in the list.

c = a.count(28)
print("28 comes",c,"times in the list.")

# Output: 28 comes 0 times in the list.