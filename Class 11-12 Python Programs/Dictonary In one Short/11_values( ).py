# This function return all the values of dictionary in the form of List. for example :
A = {1 : "One", 2 : "Two", 3 : "Three"}
B = {'A' : "Apple", 'B' : "Bat", 'C' : "Cat", 'D' : "Doll"}
print(A.values())
print(B.values())

# OUTPUT: dict_values(['One', 'Two', 'Three'])
# dict_values(['Apple', 'Bat', 'Cat', 'Doll'])

