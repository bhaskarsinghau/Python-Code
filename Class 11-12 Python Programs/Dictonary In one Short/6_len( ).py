''' This function returns the length of Dictionary means, it returns the total number of Key-Value pairs present 
in the dictionary. for example: ''' 

A = {1 : "One", 2 : "Two", 3 : "Three"}
B = {'A' : "Apple", 'B' : "Bat", 'C' : "Cat", 'D' : "Doll"}
print("Length of dictionary A is :", len(A))
print("Length of dictionary B is :", len(B))

# Output: Length of dictionary A is : 3
# Length of dictionary B is : 4

