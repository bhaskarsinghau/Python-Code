# This function return all the keys of dictionary in the form of List. for example :
A = {1 : "One", 2 : "Two", 3 : "Three"}
B = {'A' : "Apple", 'B' : "Bat", 'C' : "Cat", 'D' : "Doll"}
print(A.keys())
print(B.keys())

# OUTPUT: dict_keys([1, 2, 3])
# dict_keys(['A', 'B', 'C', 'D'])

