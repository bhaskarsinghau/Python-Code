# Create tuple from a set of a value is called packing.
t = (1,2,'a','b')

# and its reverse ; as per follwing syntax.
      # <variable>,<variable>,<variable>,<variable>,......... = t
w,x,y,z = t

# Creating individual value from a tuple's element is called unpacking.

print(w) # 1
print(x) # 2
print(y) # a
print(z) # b

