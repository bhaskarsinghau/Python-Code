# Concatenation + (tuple + tuple)
l1 = (1,3,5)
l2 = (6,7,8)
print("The sum of",l1,"and",l2,"is: ",l1+l2)        
# Output: The sum of (1, 3, 5) and (6, 7, 8) is: 
# (1, 3, 5, 6, 7, 8)

