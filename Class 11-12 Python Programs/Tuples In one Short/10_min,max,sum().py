# min(), max(), sum()

val = (17, 24, 15, 30)
print("The Minimum value of the  tuple:",val,":",min(val))
print("The Maximum Value of the tuple:",val,":",max(val))
print("The Sum of the tuple:",val,":",sum(val))

# Output:
#  The Minimum value of the  tuple: (17, 24, 15, 30) : 15
# The Maximum Value of the tuple: (17, 24, 15, 30) : 30
# The Sum of the tuple: (17, 24, 15, 30) : 86

