#it returns true or false
#at least one Character is alphabetic - True

#if characters is in the string are alphanumeric - FALSE

a = ("abc123")
b = (a.isalpha())
print(b)
# Output: False

#if characters is in the string are alphabetic - True

a = "Bhaskar"
b = (a.isalpha())
print(b)
# Output: True

#if characters is in the string are not alphabetic - FALSE

a = ""
b = (a.isalpha())
print(b)
# Output: False
